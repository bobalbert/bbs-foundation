function custom_post_css() {
	echo "<style type='text/css' media='screen'>
       #adminmenu .menu-icon-bbs_plant div.wp-menu-image:before {
            font-family:  FontAwesome !important;
            content: '\\f1bb'; // this is where you enter the fontaweseom font code
        }
     </style>";
}
